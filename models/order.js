var mongoose=require('../mongoose');
var schemaOrder=mongoose.Schema({
	cart:[{
		name:{
			type:String,
			require:true
		},
		model:{
			type:String,
			require:true,
		},
		category:{
			type:String,
			require:true
		},
		count:{
			type:Number,
			require:true
		},
		newcount:{
			type:Number,
			require:true
		},
		price:{
			type:Number,
			require:true
		},
		newprice:{
			type:Number,
			require:true
		}
	}],
	order:{
		names:{
			type:String,
			require:true
		},
		city:{
			type:String,
			require:true
		},
		email:{
			type:String,
			require:true,
			unique:true
		},
		phone:{
			type:String,
			require:true
		},
		carrier:{
			type:String,
			require:true
		},
		adress:{
			type:String,
			require:true
		},
		comment:{
			type:String
			
		}
	}
});
var order=mongoose.model("order",schemaOrder);
module.exports=order;